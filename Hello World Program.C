// Include the standard input-output library
// This provides functions like printf() for printing output
#include <stdio.h>

// Every C program must have a main() function
// Execution of the program starts from here
int main() {
    // printf() displays the string inside quotation marks
    // \n is the newline character - moves cursor to next line
    printf("Hello, World!\n");
    
    // Return 0 indicates successful program execution
    // Non-zero values usually indicate errors
    return 0;
}
